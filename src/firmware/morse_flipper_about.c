static uint8_t morse_flipper_about_request_start(uint8_t day) {
    return day % COUNT_OF(morse_flipper_about_request_seq);
}

static void morse_flipper_about_footer_draw(Canvas* canvas, uint8_t footer_i) {
    const MorseFlipperAboutFooter* f;
    int y;
    int x;

    f = &morse_flipper_about_footer[footer_i % COUNT_OF(morse_flipper_about_footer)];
    y = 36;

    x = 32 + (96 - f->a->w) / 2;
    canvas_draw_xbm(canvas, x, y + f->a->y, f->a->w, f->a->h, f->a->data);

    x = 32 + (96 - f->b->w) / 2;
    canvas_draw_xbm(canvas, x, y + f->a->h + 2 + f->b->y, f->b->w, f->b->h, f->b->data);
}

void morse_flipper_about_reset(MorseFlipperApp* app, uint32_t now_ms) {
    DateTime dt;

    if(app == NULL) return;

    furi_hal_rtc_get_datetime(&dt);
    app->about_mode = MorseFlipperAboutModeLanding;
    app->about_scroll = 0U;
    app->about_ok_count = 0U;
    app->about_last_ok_ms = 0U;
    app->about_footer_seq_i = morse_flipper_about_request_start(dt.day);
    app->about_social_idx = morse_flipper_about_request_seq[app->about_footer_seq_i];
    app->about_show_next = false;
    app->about_social_next_ms = now_ms + MORSE_FLIPPER_ABOUT_ALLOW_MS;
}

void morse_flipper_tick_about(MorseFlipperApp* app, uint32_t now_ms) {
    if(app == NULL || app->screen != MorseFlipperScreenAbout) return;
    if(app->about_mode != MorseFlipperAboutModeLanding) return;
    if(now_ms < app->about_social_next_ms) return;

    if(!app->about_show_next) {
        app->about_show_next = true;
    } else {
        app->about_footer_seq_i++;
        if(app->about_footer_seq_i >= COUNT_OF(morse_flipper_about_request_seq))
            app->about_footer_seq_i = 0U;
        app->about_social_idx = morse_flipper_about_request_seq[app->about_footer_seq_i];
    }

    app->about_social_next_ms = now_ms + MORSE_FLIPPER_ABOUT_CYCLE_MS;
    morse_flipper_view_dirty(app);
}

static uint16_t morse_flipper_about_word_width(Canvas* canvas, const char* start, size_t len) {
    char word[24];
    size_t n = len < sizeof(word) - 1U ? len : sizeof(word) - 1U;

    memcpy(word, start, n);
    word[n] = '\0';

    return canvas_string_width(canvas, word);
}

static size_t morse_flipper_about_next_line(
    Canvas* canvas,
    const char* text,
    size_t pos,
    char* out,
    size_t out_sz) {
    size_t out_len = 0U;
    uint16_t width = 0U;

    while(text[pos] == ' ') pos++;

    while(text[pos] != '\0') {
        size_t word_len = 0U;
        uint16_t word_width;
        uint16_t sep_width = out_len > 0U ? canvas_string_width(canvas, " ") : 0U;

        if(text[pos] == '\n') {
            pos++;
            break;
        }

        while(text[pos + word_len] != '\0' && text[pos + word_len] != ' ' &&
              text[pos + word_len] != '\n') {
            word_len++;
        }

        word_width = morse_flipper_about_word_width(canvas, &text[pos], word_len);
        if(out_len > 0U && (uint16_t)(width + sep_width + word_width) > 120U) break;

        if(out_len > 0U && out_len + 1U < out_sz) {
            out[out_len++] = ' ';
            width += sep_width;
        }

        if(out_len + word_len < out_sz) {
            memcpy(&out[out_len], &text[pos], word_len);
            out_len += word_len;
        }
        width += word_width;
        pos += word_len;

        while(text[pos] == ' ') pos++;
        if(text[pos] == '\n') {
            pos++;
            break;
        }
    }

    out[out_len < out_sz ? out_len : out_sz - 1U] = '\0';
    return pos;
}

static uint8_t morse_flipper_about_line_count(Canvas* canvas) {
    size_t pos = 0U;
    uint8_t lines = 0U;
    char wrapped[32];

    if(canvas == NULL) return 0U;
    canvas_set_font(canvas, FontSecondary);
    while(morse_flipper_about_body[pos] != '\0') {
        pos = morse_flipper_about_next_line(
            canvas, morse_flipper_about_body, pos, wrapped, sizeof(wrapped));
        lines++;
    }

    return lines;
}

uint8_t morse_flipper_about_max_scroll(Canvas* canvas) {
    uint8_t lines = morse_flipper_about_line_count(canvas);

    return lines > 6U ? (uint8_t)(lines - 6U) : 0U;
}

static void morse_flipper_about_draw_text(Canvas* canvas, uint8_t scroll) {
    size_t pos = 0U;
    uint8_t line = 0U;
    uint8_t skipped = 0U;
    char wrapped[32];

    canvas_set_font(canvas, FontSecondary);
    while(morse_flipper_about_body[pos] != '\0' && line < 6U) {
        pos = morse_flipper_about_next_line(
            canvas, morse_flipper_about_body, pos, wrapped, sizeof(wrapped));
        if(skipped < scroll) {
            skipped++;
            continue;
        }

        canvas_draw_str(canvas, 3, (int32_t)(9U + (line * 8U)), wrapped);
        line++;
    }
}

static void morse_flipper_draw_about_landing(Canvas* canvas, const MorseFlipperApp* app) {
    canvas_clear(canvas);
    canvas_draw_xbm(canvas, 4, 16, 32, 32, morse_flipper_about_left_icon);
    canvas_draw_xbm(canvas, 45, 8, 70, 16, morse_flipper_about_yo3gnd);
    morse_flipper_about_footer_draw(canvas, app->about_social_idx);

    if(app->about_show_next) elements_button_right(canvas, "Next");
}

static void morse_flipper_draw_about_text_view(Canvas* canvas, const MorseFlipperApp* app) {
    canvas_clear(canvas);
    morse_flipper_about_draw_text(canvas, app->about_scroll);
    elements_button_left(canvas, "Back");
}

static void morse_flipper_draw_about_easter(Canvas* canvas) {
    canvas_clear(canvas);
    canvas_set_font(canvas, FontPrimary);
    canvas_draw_str_aligned(canvas, 64, 18, AlignCenter, AlignCenter, "Richard,");
    canvas_draw_str_aligned(canvas, 64, 33, AlignCenter, AlignCenter, "this nonsense");
    canvas_draw_str_aligned(canvas, 64, 48, AlignCenter, AlignCenter, "again.");
    elements_button_left(canvas, "Back");
}

void morse_flipper_draw_about(Canvas* canvas, const MorseFlipperApp* app) {
    if(canvas == NULL || app == NULL) return;

    if(app->about_mode == MorseFlipperAboutModeText) {
        morse_flipper_draw_about_text_view(canvas, app);
        return;
    }

    if(app->about_mode == MorseFlipperAboutModeEaster) {
        morse_flipper_draw_about_easter(canvas);
        return;
    }

    morse_flipper_draw_about_landing(canvas, app);
}
